    max_index = max(len(REAL_DESTINATION_ROWS) - 1, 1)
    for index, (name, category, province, city, lng, lat, tags) in enumerate(REAL_DESTINATION_ROWS):
        rating = min(4.95, 4.35 + (max_index - index) / max_index * 0.45 + (0.04 if category == "scenic" else 0))
        popularity = 10000 + (max_index - index) * 37 + (800 if category == "scenic" else 400)
        rows.append(
            {
                "name": name,
                "category": category,
                "province": province,
                "city": city,
                "lng": lng,
                "lat": lat,
                "rating": round(rating, 2),
                "popularity": popularity,
                "tags": sorted(set([category, province, city, *tags])),
            }
        )
    return rows
